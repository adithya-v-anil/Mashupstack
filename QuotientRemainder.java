public class QuotientRemainder {
    public static void main(String args[]) {
        int divident = 51;
        int divisor = 5;
        int quotient = divident / divisor;
        int remainder = divident % divisor;
        System.out.println("Quotient = " +quotient);
        System.out.println("Remainder = " +remainder);
    }
}
